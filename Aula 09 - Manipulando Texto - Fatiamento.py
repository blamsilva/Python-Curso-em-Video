frase = 'Curso em Video Python'
'''Essa Frase foi dividida em 21 espaços no de 0 a 20'''
'''Fateamento de string'''
print(frase[9])
'''imprime apenas o espaço 9'''
print(frase[9:13])
'''imprime do espaço 9 até o 12 (sempre vai do primeiro até o anterior ao ultimo descrito(9:13)'''
print(frase[9:21:2])
'''imprime do espaço 9 até o 20 e pula de 2 em 2'''
print(frase[:5])
'''omite o inicio ai ele começa do espaço inicial e vai até a o espaço 4'''
print(frase[15:])
'''omite o final ele vai do espaço selecionado até o espaço final'''
print(frase[9::3])
'''Começa do espaço 9 vai até o final pulando de 3 em 3'''


